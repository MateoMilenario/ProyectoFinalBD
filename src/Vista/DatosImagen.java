    private void guardarIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarIActionPerformed

        ControlImagen CI2 = new ControlImagen();

        NombreImagen = nombreImagen.getText();
        FormatoActual = formatoImagen.getSelectedItem().toString();
        CategoriaActual = categorias.getSelectedItem().toString();
        Resolucion = resolucion.getText();
        DescripcionImagen = descripcionImagen.getText();

        if (CategoriaActual.equals("----------------------")) {

            CategoriaActual = categoriaActual.getText();

        }//Fin if

        if (FormatoActual.equals("-----------------------")) {

            FormatoActual = formatoActual.getText();

        }//Fin if

        //*************************************************************************************************************************
        //Se actualiza los valores de la imagen, siempre y cuando el usuario haya realizado una actualización desde la aplicación
        imagenesCuenta.get(contador).setDescripcionImagen(DescripcionImagen);
        imagenesCuenta.get(contador).setFormato_Imagen(FormatoActual);
        imagenesCuenta.get(contador).setNombreImagen(NombreImagen);
        imagenesCuenta.get(contador).setResolucion(Resolucion);

        Imagen imagenActualizar = imagenesCuenta.get(contador);
        String pkCategoria = "";

        for (int i = 0; i < categoriasImagenes.size(); i++) {

            if (categoriasImagenes.get(i).getNombreCategoria().equals(CategoriaActual)) {

                pkCategoria = categoriasImagenes.get(i).getCodigoCategoria();
                imagenesCuenta.get(contador).setCategoriaImagen(CategoriaActual);

            }//Fin if

        }//Fin for

        //*************************************************************************************************************************
        boolean t = CI2.ActualizarDatos(imagenesCuenta.get(contador).getIdImagen(), imagenActualizar, pkCategoria);

        if (t) {

            JOptionPane.showMessageDialog(null, "Datos actualizados.");

        } else {

            JOptionPane.showMessageDialog(null, "Datos no actualizados.");

        }//Fin if

    }//GEN-LAST:event_guardarIActionPerformed

