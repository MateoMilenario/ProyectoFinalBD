    //Método para mostrar las imagenes en la aplicación
    public void MostrarImagenes() {

        fechaSubida.setText(imagenesCuenta.get(contador).getFechaSubidaI());
        nombreImagen.setText(imagenesCuenta.get(contador).getNombreImagen());
        formatoActual.setText(imagenesCuenta.get(contador).getFormato_Imagen());
        resolucion.setText(imagenesCuenta.get(contador).getResolucion());
        categoriaActual.setText(imagenesCuenta.get(contador).getCategoriaImagen());

        descripcionImagen.setText(imagenesCuenta.get(contador).getDescripcionImagen());

        ImagenSubida = imagenesCuenta.get(contador).getImagenCuenta();

        //Se pasa la imagen que está en un arreglo de byte para convertirlo en un icono
        ImageIcon icon = new ImageIcon(ImagenSubida);

        //Después de que la imagen está convertida en icono entonces se procede a escalar el icono con el tamaño que conserva el label
        Icon icono = new ImageIcon(icon.getImage().getScaledInstance(LabelImagen.getWidth(), LabelImagen.getHeight(), Image.SCALE_DEFAULT));

        //Se coloca la imagen con el tamaño que tiene en el label
        LabelImagen.setIcon(icono);

    }//Fin método

    public void TraerCategorias() {

        //Se trae todas las categorías pertenecientes a la BD
        ControlCategoria CC2 = new ControlCategoria();

        categoriasImagenes = CC2.LeerCategorias();

        for (int i = 0; i < categoriasImagenes.size(); i++) {

            categorias.addItem(categoriasImagenes.get(i).getNombreCategoria());

        }//Fin for

    }//Fin método

    public void TraerImagenesCuenta() {

        ControlImagen CI = new ControlImagen();

        //Devuelve un linkelist donde se encuentra guardado todos los datos de las imagenes de la cuenta
        imagenesCuenta = CI.TraerImagenes(NombreUsuarioImagenes);

        if (imagenesCuenta.isEmpty() == false) {

            MostrarImagenes();

            elminarImagen.setEnabled(true);
            guardarI.setEnabled(true);
            elminarImagen.setEnabled(true);
            guardarI.setEnabled(true);

        } else {

            JOptionPane.showMessageDialog(null, "No has agregado contenido de imagen a tu cuenta." + "\n\nNota: puedes agregar imágenes presionando la pestaña" + "\n[Imagen] que se encuentra en la ventana principal.");
            elminarImagen.setEnabled(false);
            guardarI.setEnabled(false);

        }//Fin if

    }//Fin método

