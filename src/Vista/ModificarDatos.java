    private void MostrarDatosModificar() {

        ControlCuenta cCuenta = new ControlCuenta();

        CuentaUsuario cUsuario = cCuenta.LeerCuenta(NombreUsuarioModificar);

        //nombreUsuario2.setText(cUsuario.getNombreUsuario());
        EditarNombre1.setText(cUsuario.getNombre1c());
        EditarNombre2.setText(cUsuario.getNombre2c());
        EditarApellido1.setText(cUsuario.getApellido1c());
        EditarApellido2.setText(cUsuario.getApellido2c());
        EditarEmail.setText(cUsuario.getEmail());
        generoActual.setText(cUsuario.getGenero());
        fechaNActural.setText(cUsuario.getFecha_Nacimiento());
        contraseñaActual.setText(cUsuario.getContrasena());
        FechaCreacion = cUsuario.getFecha_Creacion();

        //**********************************************************************************************************************************
        ImagenPerfil = cUsuario.getFotoPerfil();

        //Se pasa la imagen que está en un arreglo de byte para convertirlo en un icono
        ImageIcon icon = new ImageIcon(ImagenPerfil);

        //Después de que la imagen está convertida en icono entonces se procede a escalar el icono con el tamaño que conserva el label
        Icon icono = new ImageIcon(icon.getImage().getScaledInstance(EditarLabelFoto.getWidth(), EditarLabelFoto.getHeight(), Image.SCALE_DEFAULT));

        //Se coloca la imagen con el tamaño que tiene en el label
        EditarLabelFoto.setIcon(icono);

    }//Fin método

