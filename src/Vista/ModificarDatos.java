    private void cambiarFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cambiarFotoActionPerformed

        int Resultado = JOptionPane.showConfirmDialog(null, "¿Está seguro/a de cambiar la foto de perfil?");

        if (Resultado == 0) {

            if (seleccionador.showDialog(null, null) == JFileChooser.APPROVE_OPTION) {

                archivo = seleccionador.getSelectedFile();

                if (archivo.canRead()) {

                    //El endsWith especifica si la cadena que compone el archivo imagen termina en jpg o png para ser verificada.
                    if (archivo.getName().endsWith("jpg") || archivo.getName().endsWith("png")) {

                        ImageIcon icon = new ImageIcon(archivo.toString());

                        Icon icono = new ImageIcon(icon.getImage().getScaledInstance(EditarLabelFoto.getWidth(), EditarLabelFoto.getHeight(), Image.SCALE_DEFAULT));

                        //Se coloca la imagen con el tamaño que tiene el labol
                        EditarLabelFoto.setIcon(icono);

                    } else {

                        JOptionPane.showMessageDialog(null, "El Formato de la imagen no aceptado. Se recomienda: jpg o png.", "Error", JOptionPane.ERROR_MESSAGE);

                    }//Fin if

                }//Fin segundo if

            }//Fin primer if

        }//Fin if verificación

    }//GEN-LAST:event_cambiarFotoActionPerformed

