    public void MostrarVideos() {

        fechaSubida.setText(videosCuenta.get(contador).getFechaSubidaV());
        nombreVideo.setText(videosCuenta.get(contador).getNombreVideo());
        formatoActual.setText(videosCuenta.get(contador).getFormato_Video());

        String duracion = videosCuenta.get(contador).getDuracionVideo();
        String[] DuracionTotal = duracion.split(":");

        duracionHoras.setText(DuracionTotal[0]);
        duracionMinutos.setText(DuracionTotal[1]);
        duracionSegundos.setText(DuracionTotal[2]);

        categoriaActual.setText(videosCuenta.get(contador).getCategoriaVideo());

        descripcionVideo.setText(videosCuenta.get(contador).getDescripcionVideo());

        VideoSubido = videosCuenta.get(contador).getVideo();

    }//Fin método

    public void TraerCategorias() {

        ControlCategoria CC = new ControlCategoria();
        categoriasVideos = CC.LeerCategorias();

        for (int i = 0; i < categoriasVideos.size(); i++) {

            categorias.addItem(categoriasVideos.get(i).getNombreCategoria());

        }//Fin for

    }//Fin método

    public void ImagenPredeterminada() {

        ImageIcon icon = new ImageIcon(DatosVideo.class.getResource("/Imagenes/video-icon.jpg"));
        Icon icono = new ImageIcon(icon.getImage().getScaledInstance(LabelVideo.getWidth(), LabelVideo.getHeight(), Image.SCALE_DEFAULT));

        LabelVideo.setIcon(icono);

    }//Fin método

    public void MostrarVideosCuenta() {

        ControlVideo CI = new ControlVideo();

        //Devuelve un linkelist donde se encuentra guardado todos los datos de las imagenes de la cuenta
        videosCuenta = CI.TraerVideos(NombreUsuarioVideos);

        if (videosCuenta.isEmpty() == false) {

            MostrarVideos();

            guardarV.setEnabled(true);
            EliminarVideo.setEnabled(true);

        } else {

            JOptionPane.showMessageDialog(null, "No has agregado contenido de video a tu cuenta." + "\n\nNota: puedes agregar videos presionando la pestaña" + "\n[Video] que se encuentra en la ventana principal.");

            EliminarVideo.setEnabled(false);
            guardarV.setEnabled(false);

        }//Fin if

    }//Fin método

