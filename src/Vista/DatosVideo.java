    private void guardarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarVActionPerformed

        ControlVideo CV2 = new ControlVideo();

        NombreVideo = nombreVideo.getText();
        FormatoActual = formatoVideo.getSelectedItem().toString();
        CategoriaActual = categorias.getSelectedItem().toString();
        DescripcionVideo = descripcionVideo.getText();

        String duracion = videosCuenta.get(contador).getDuracionVideo();
        String[] DuracionTotal = duracion.split(":");

        //******************************************************************************************************************
        //CONDICIONES PARA OBTENER EL TIEMPO EN FORMATO 00:00:00
        if (duracionHoras.getText().equals("")) {

            Duracion = "00";

        } else {

            switch (duracionHoras.getText().length()) {

                case 1:

                    Duracion = "0" + duracionHoras.getText();
                    break;

                case 2:

                    Duracion = duracionHoras.getText();
                    break;

                default:

                    JOptionPane.showMessageDialog(null, "Hora no válida", "Advertencia", JOptionPane.ERROR_MESSAGE);
                    Duracion = DuracionTotal[0];
                    duracionHoras.setText(DuracionTotal[0]);
                    break;
            }

        }//Fin if

        if (duracionMinutos.getText().equals("")) {

            Duracion = Duracion + ":00";

        } else {

            if (duracionMinutos.getText().length() == 1) {
                Duracion = Duracion + ":0" + duracionMinutos.getText();

            } else if (duracionMinutos.getText().length() == 2 && Integer.parseInt(duracionMinutos.getText()) <= 60) {

                Duracion = Duracion + ":" + duracionMinutos.getText();

            } else {

                JOptionPane.showMessageDialog(null, "Minutos no válidos", "Advertencia", JOptionPane.ERROR_MESSAGE);
                Duracion = Duracion + ":" + DuracionTotal[1];
                duracionMinutos.setText(DuracionTotal[1]);

            }//Fin segundo if

        }//Fin if

        if (duracionSegundos.getText().equals("")) {

            Duracion = Duracion + ":00";

        } else {

            if (duracionSegundos.getText().length() == 1) {

                Duracion = Duracion + ":0" + duracionSegundos.getText();

            } else if (duracionSegundos.getText().length() == 2 && Integer.parseInt(duracionSegundos.getText()) <= 60) {

                Duracion = Duracion + ":" + duracionSegundos.getText();

            } else {

                JOptionPane.showMessageDialog(null, "Segundos no válidos", "Advertencia", JOptionPane.ERROR_MESSAGE);
                Duracion = Duracion + ":" + DuracionTotal[2];
                duracionSegundos.setText(DuracionTotal[2]);

            }//Fin segundo if

        }//Fin if

        //******************************************************************************************************************
        if (CategoriaActual.equals("----------------------")) {

            CategoriaActual = categoriaActual.getText();

        }//Fin if

        if (FormatoActual.equals("-----------------------")) {

            FormatoActual = formatoActual.getText();

        }//Fin if

        //*************************************************************************************************************************
        //Se actualiza los valores del video, siempre y cuando el usuario haya realizado una actualización desde la aplicación
        videosCuenta.get(contador).setNombreVideo(NombreVideo);
        videosCuenta.get(contador).setFormato_Video(FormatoActual);
        videosCuenta.get(contador).setDuracionVideo(Duracion);
        videosCuenta.get(contador).setDescripcionVideo(DescripcionVideo);

        Video videoActualizar = videosCuenta.get(contador);
        String pkCategoria = "";

        for (int i = 0; i < categoriasVideos.size(); i++) {

            if (categoriasVideos.get(i).getNombreCategoria().equals(CategoriaActual)) {

                pkCategoria = categoriasVideos.get(i).getCodigoCategoria();
                videosCuenta.get(contador).setCategoriaVideo(CategoriaActual);

            }//Fin if

        }//Fin for

        //*************************************************************************************************************************
        boolean t = CV2.ActualizarDatos(videosCuenta.get(contador).getIdContenidoV(), videoActualizar, pkCategoria);

        if (t) {

            JOptionPane.showMessageDialog(null, "Datos actualizados.");

        } else {

            JOptionPane.showMessageDialog(null, "Datos no actualizados.");

        }//Fin if

    }//GEN-LAST:event_guardarVActionPerformed

    private void EliminarVideoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarVideoActionPerformed

        int respuesta = JOptionPane.showConfirmDialog(null, "¿Está seguro/a que desea eliminar el video?");

        if (respuesta == 0) {

            ControlVideo CI3 = new ControlVideo();
            ControlComentarioVideo CcomentarioV = new ControlComentarioVideo();

            String IdContenido = videosCuenta.get(contador).getIdContenidoV();
            comentariosVideos = CcomentarioV.TraerComentarios(IdContenido);
            EliminarComentariosVideos();

            boolean t = CI3.EliminarVideo(IdContenido);

            if (t) {

                JOptionPane.showMessageDialog(null, "Datos del video eliminados");
                //Se elimina el objeto de la imagen que quedó en el linkedlist
                videosCuenta.remove(contador);
                LabelVideo.setIcon(null);
                fechaSubida.setText("");
                formatoActual.setText("");
                categoriaActual.setText("");
                duracionHoras.setText("");
                duracionMinutos.setText("");
                duracionSegundos.setText("");
                descripcionVideo.setText("");
                nombreVideo.setText("");
                
                if(comentariosVideos.isEmpty() == false){
                    
                    comentariosVideos.clear();
                    
                }//Fin condición para limpiar el LinkedList

            } else {

                JOptionPane.showMessageDialog(null, "Datos del video no eliminados");

            }//Fin if

        }//Fin if principal

    }//GEN-LAST:event_EliminarVideoActionPerformed

